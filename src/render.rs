    out.push_str("mark_gpu_collector_unavailable() {\n");
    out.push_str("  GPU_COLLECTOR_AVAILABLE=0\n");
    out.push_str("  GPU_COLLECTOR_NOTE=$1\n");
    out.push_str("  write_metrics_meta\n");
