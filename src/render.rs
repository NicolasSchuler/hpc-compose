fn render_service(out: &mut String, service: &RuntimeService, dependents: &[String]) {
    let service_id = service_token(&service.name);
    let fn_name = format!("launch_{service_id}");
    let step_name = service_step_name(&service.name);
    let command_args = execution_argv(&service.execution, service.working_dir.as_deref());
    let srun_args = build_srun_command(service);
    let dependents_csv = dependents.join(",");
    let service_env = service
        .environment
        .iter()
        .map(|(k, v)| format!("{k}={v}"))
        .collect::<Vec<_>>();

    out.push_str(&format!("{fn_name}() {{\n"));
    out.push_str(&format!(
        "  local logfile=\"$LOG_DIR/{}\"\n",
        log_file_name_for_service(&service.name)
    ));
    out.push_str(&format!(
        "  local -a service_mounts={}\n",
        bash_array_literal(&service.volumes)
    ));
    out.push_str(&format!(
        "  local -a srun_cmd={}\n",
        bash_array_literal(&srun_args)
    ));
    out.push_str(&format!(
        "  local -a service_cmd={}\n",
        bash_array_literal(&command_args)
    ));
    out.push_str("  local pyxis_mounts\n");
    out.push_str("  pyxis_mounts=$(build_pyxis_mounts \"${service_mounts[@]}\")\n");
    out.push_str("  srun_cmd+=(--container-image=");
    out.push_str(&shell_quote(&service.runtime_image.display().to_string()));
    out.push_str(")\n");
    out.push_str("  if [[ -n \"$pyxis_mounts\" ]]; then\n");
    out.push_str("    srun_cmd+=(\"--container-mounts=$pyxis_mounts\")\n");
    out.push_str("  fi\n");
    if service.placement.pin_to_primary_node {
        out.push_str("  local service_nodelist=\"$HPC_COMPOSE_PRIMARY_NODE\"\n");
        out.push_str("  srun_cmd+=(\"--nodelist=$service_nodelist\")\n");
    } else {
        out.push_str("  local service_nodelist=\"$HPC_COMPOSE_NODELIST\"\n");
    }
    out.push_str("  echo \"Starting service ");
    out.push_str(&service.name);
    out.push_str("\"\n");
    out.push_str("  local -a launch_env=()\n");
    out.push_str("  launch_env+=(\"HPC_COMPOSE_PRIMARY_NODE=$HPC_COMPOSE_PRIMARY_NODE\")\n");
    out.push_str("  launch_env+=(\"HPC_COMPOSE_NODE_COUNT=$HPC_COMPOSE_NODE_COUNT\")\n");
    out.push_str("  launch_env+=(\"HPC_COMPOSE_NODELIST=$HPC_COMPOSE_NODELIST\")\n");
    out.push_str("  launch_env+=(\"HPC_COMPOSE_NODELIST_FILE=$HPC_COMPOSE_NODELIST_FILE\")\n");
    out.push_str("  if [[ \"$RESUME_ENABLED\" == \"1\" ]]; then\n");
    out.push_str("    launch_env+=(\"HPC_COMPOSE_RESUME_DIR=$RESUME_CONTAINER_PATH\")\n");
    out.push_str("    launch_env+=(\"HPC_COMPOSE_ATTEMPT=$ATTEMPT\")\n");
    out.push_str("    launch_env+=(\"HPC_COMPOSE_IS_RESUME=$IS_RESUME\")\n");
    out.push_str("  fi\n");
    if !service.environment.is_empty() {
        out.push_str(&format!(
            "  local -a service_env={}\n",
            bash_array_literal(&service_env)
        ));
        out.push_str("  launch_env+=(\"${service_env[@]}\")\n");
    }
    out.push_str("  if (( ${#launch_env[@]} == 0 )); then\n");
    out.push_str("    \"${srun_cmd[@]}\" \"${service_cmd[@]}\" >\"$logfile\" 2>&1 &\n");
    out.push_str("  else\n");
    out.push_str("    env \"${launch_env[@]}\" \"${srun_cmd[@]}\" \"${service_cmd[@]}\" >\"$logfile\" 2>&1 &\n");
    out.push_str("  fi\n");
    out.push_str("  local pid=$!\n");
    out.push_str(&format!(
        "  register_service {} \"$pid\" {} \"$logfile\" {} {} {} {} {} {} {} {} {} {} {} {} {}\n",
        shell_quote(&service.name),
        shell_quote(&step_name),
        shell_quote(failure_policy_mode_label(service.failure_policy.mode)),
        service.failure_policy.max_restarts,
        service.failure_policy.backoff_seconds,
        service.failure_policy.window_seconds,
        service.failure_policy.max_restarts_in_window,
        shell_quote(&fn_name),
        shell_quote(&dependents_csv),
        shell_quote(placement_mode_label(service.placement.mode)),
        service.placement.nodes,
        shell_quote(
            &service
                .placement
                .ntasks
                .map(|value| value.to_string())
                .unwrap_or_default(),
        ),
        shell_quote(
            &service
                .placement
                .ntasks_per_node
                .map(|value| value.to_string())
                .unwrap_or_default(),
        ),
        "\"$service_nodelist\"",
        if service.readiness.is_some() {
            "1"
        } else {
            "0"
        }
    ));
    out.push_str("}\n");
}

fn render_readiness_wait(out: &mut String, service: &RuntimeService) {
    let fn_name = format!("wait_until_{}_ready", service_token(&service.name));
    out.push_str(&format!("{fn_name}() {{\n"));
    out.push_str("  local pid=$1\n");
    out.push_str("  local name=$2\n");
    if let Some(readiness) = &service.readiness {
        match readiness {
            ReadinessSpec::Sleep { seconds } => {
                out.push_str(&format!("  wait_for_sleep \"$pid\" \"$name\" {seconds}\n"));
            }
            ReadinessSpec::Tcp {
                host,
                port,
                timeout_seconds,
            } => {
                let host = host.as_deref().unwrap_or("127.0.0.1");
                let timeout = timeout_seconds.unwrap_or(60);
                out.push_str(&format!(
                    "  wait_for_tcp \"$pid\" \"$name\" {} {} {}\n",
                    shell_quote(host),
                    port,
                    timeout
                ));
            }
            ReadinessSpec::Log {
                pattern,
                timeout_seconds,
            } => {
                let timeout = timeout_seconds.unwrap_or(60);
                out.push_str(&format!(
                    "  wait_for_log \"$pid\" \"$name\" \"$LOG_DIR/{}\" {} {}\n",
                    log_file_name_for_service(&service.name),
                    shell_quote(pattern),
                    timeout
                ));
            }
            ReadinessSpec::Http {
                url,
                status_code,
                timeout_seconds,
            } => {
                let timeout = timeout_seconds.unwrap_or(60);
                out.push_str(&format!(
                    "  wait_for_http \"$pid\" \"$name\" {} {} {}\n",
                    shell_quote(url),
                    status_code,
                    timeout
                ));
            }
        }
    } else {
        out.push_str("  :\n");
    }
    out.push_str("}\n");
}

fn render_dependency_waits(out: &mut String, service: &RuntimeService) {
    for dependency in &service.depends_on {
        match dependency.condition {
            DependencyCondition::ServiceStarted => out.push_str(&format!(
                "wait_for_service_started {} {}\n",
                shell_quote(&dependency.name),
                shell_quote(&service.name)
            )),
            DependencyCondition::ServiceHealthy => out.push_str(&format!(
                "wait_for_service_healthy {} {} wait_until_{}_ready\n",
                shell_quote(&dependency.name),
                shell_quote(&service.name),
                service_token(&dependency.name)
            )),
        }
    }
}

/// Converts an [`ExecutionSpec`] into the argv used inside the container.
pub fn execution_argv(execution: &ExecutionSpec, working_dir: Option<&str>) -> Vec<String> {
    match (execution, working_dir) {
        (ExecutionSpec::ImageDefault, None) => Vec::new(),
        (ExecutionSpec::Shell(script), None) => {
            vec!["/bin/sh".into(), "-lc".into(), script.clone()]
        }
        (ExecutionSpec::Exec(argv), None) => argv.clone(),
        (ExecutionSpec::Shell(script), Some(dir)) => vec![
            "/bin/sh".into(),
            "-lc".into(),
            "cd \"$1\" && shift && exec /bin/sh -lc \"$1\"".into(),
            "hpc-compose".into(),
            dir.to_string(),
            script.clone(),
        ],
        (ExecutionSpec::Exec(argv), Some(dir)) => {
            let mut wrapped = vec![
                "/bin/sh".into(),
                "-lc".into(),
                "cd \"$1\" && shift && exec \"$@\"".into(),
                "hpc-compose".into(),
                dir.to_string(),
            ];
            wrapped.extend(argv.clone());
            wrapped
        }
        (ExecutionSpec::ImageDefault, Some(_)) => unreachable!("validated earlier"),
    }
}

/// Builds the `srun` command line for one runtime service.
pub fn build_srun_command(service: &RuntimeService) -> Vec<String> {
    let mut args = vec![
        "srun".to_string(),
        format!("--nodes={}", service.placement.nodes),
        "--exact".to_string(),
        "--overlap".to_string(),
        format!("--job-name={}", service_step_name(&service.name)),
    ];
    if let Some(ntasks) = service.placement.ntasks {
        args.push(format!("--ntasks={ntasks}"));
    }
    if let Some(ntasks_per_node) = service.placement.ntasks_per_node {
        args.push(format!("--ntasks-per-node={ntasks_per_node}"));
    }
    if matches!(service.execution, ExecutionSpec::ImageDefault) {
        args.push("--container-entrypoint".to_string());
    }
    let mut env_names = vec![
        "HPC_COMPOSE_PRIMARY_NODE",
        "HPC_COMPOSE_NODE_COUNT",
        "HPC_COMPOSE_NODELIST",
        "HPC_COMPOSE_NODELIST_FILE",
        "HPC_COMPOSE_RESUME_DIR",
        "HPC_COMPOSE_ATTEMPT",
        "HPC_COMPOSE_IS_RESUME",
    ];
    env_names.extend(service.environment.iter().map(|(name, _)| name.as_str()));
    env_names.sort_unstable();
    env_names.dedup();
    args.push(format!("--container-env={}", env_names.join(",")));
    if let Some(cpus) = service.slurm.cpus_per_task {
        args.push(format!("--cpus-per-task={cpus}"));
    }
    if let Some(gres) = &service.slurm.gres {
        args.push(format!("--gres={gres}"));
    } else if let Some(gpus) = service.slurm.gpus {
        args.push(format!("--gpus={gpus}"));
    }
    args.extend(service.slurm.extra_srun_args.clone());
    args
}

/// Builds the user-visible `srun` command line for one runtime service.
pub fn display_srun_command(service: &RuntimeService) -> Vec<String> {
    let mut args = build_srun_command(service);
    if service.placement.pin_to_primary_node {
        args.push("--nodelist=$HPC_COMPOSE_PRIMARY_NODE".to_string());
    }
    args
}

fn bash_array_literal(items: &[String]) -> String {
    let body = items
        .iter()
        .map(|item| shell_quote(item))
        .collect::<Vec<_>>()
        .join(" ");
    format!("({body})")
}

fn service_token(value: &str) -> String {
    let mut token = String::new();
    for byte in value.bytes() {
        if byte.is_ascii_alphanumeric() {
            token.push(byte as char);
        } else {
            token.push_str(&format!("_x{byte:02x}_"));
        }
    }
    token
}

fn service_step_name(value: &str) -> String {
    format!("hpc-compose:{}", service_token(value))
}

/// Converts a service name into the tracked log file name used on disk.
pub fn log_file_name_for_service(value: &str) -> String {
    format!("{}.log", service_token(value))
}

fn shell_quote(value: &str) -> String {
    if value.is_empty() {
        return "''".to_string();
    }
    let escaped = value.replace('\'', "'\"'\"'");
    format!("'{escaped}'")
}

fn flag(value: bool) -> &'static str {
    if value { "1" } else { "0" }
}

fn failure_policy_mode_label(mode: ServiceFailureMode) -> &'static str {
    match mode {
        ServiceFailureMode::FailJob => "fail_job",
        ServiceFailureMode::Ignore => "ignore",
        ServiceFailureMode::RestartOnFailure => "restart_on_failure",
    }
}

fn placement_mode_label(mode: ServicePlacementMode) -> &'static str {
    match mode {
        ServicePlacementMode::PrimaryNode => "primary_node",
        ServicePlacementMode::Distributed => "distributed",
    }
}

