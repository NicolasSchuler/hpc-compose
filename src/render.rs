    out.push_str("  value=${value//\\\\/\\\\\\\\}\n");
    out.push_str("  value=${value//\\\"/\\\\\\\"}\n");
    out.push_str("  value=${value//$'\\n'/\\\\n}\n");
    out.push_str("  value=${value//$'\\r'/\\\\r}\n");
